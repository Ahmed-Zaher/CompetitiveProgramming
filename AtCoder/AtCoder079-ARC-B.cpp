/*
 * Note that if we have a sequence a1, a2, ..., aN
 * and max ai - min ai <= N, and assuming that ai >= N,
 * then after N moves the resulting sequence will be
 * a1 - 1, a2 - 1, ..., aN - 1
 * Now let's use N = 50, and consider the sequence 1 2 .. 50,
 * we can apply 50 moves to this sequence to get 0 1 .. 49 and then
 * we're out of moves.
 * Now let's get K % 50 moves by using the sequence 0..49 after incrementing
 * the last K % 50 elements, and get K / 50 moves by incrementing the whole
 * sequence by K / 50 - we can observe that such solution always works
 */

